    function searchAuthorName(...$name)
    {
        if (count($name) && preg_match("/\S/", urldecode($name[0]))) {
            $authors = Author::where("name", "like", "%".$name[0]."%")->get();
            return AuthorResource::collection($authors);
        } else {
            return AuthorResource::collection([]);
